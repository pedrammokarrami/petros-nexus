export const searchMedia = (query) => {
  const q = query.toLowerCase()
  const allMedia = [
    ...mockMusic.map(m => ({ ...m, type: 'music' })),
    ...mockMovies.map(m => ({ ...m, type: 'movie' })),
    ...mockPodcasts.map(m => ({ ...m, type: 'podcast' })),
    ...mockSeries.map(m => ({ ...m, type: 'series' }))
  ]
  return allMedia.filter(item =>
    item.title?.toLowerCase().includes(q) ||
    item.title_fa?.includes(q) ||
    item.artist?.toLowerCase().includes(q) ||
    item.genre?.toLowerCase().includes(q) ||
    item.description_fa?.includes(q) ||
    item.show?.includes(q) ||
    item.host?.includes(q)
  )
}
